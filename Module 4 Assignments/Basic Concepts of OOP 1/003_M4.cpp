/*
3. What is the difference between OOP and POP?
*/

/*
#OOP (Object-Oriented Programming):

-Focuses on organizing code into objects, which are instances of classes.
-Emphasizes encapsulation, where data and methods that operate on the data are bundled together in a class.
-Supports concepts like inheritance and polymorphism for code reuse and flexibility.
-Promotes the modeling of real-world entities and relationships in the code.

#POP (Procedural-Oriented Programming):

-Organizes code as procedures or functions that perform tasks sequentially.
-Emphasizes procedures, routines, or functions that operate on data.
-Lacks the concept of classes and objects.
-Typically follows a top-down approach, dividing the program into procedures that call each other.
*/
